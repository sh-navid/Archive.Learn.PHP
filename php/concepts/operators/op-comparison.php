<?php

// Spaceship
echo 10 <=> 5, " | ", 10 <=> 10, " | ", 5 <=> 10, "\n";
// 1 | 0 | -1


// Equal
echo 10 == "10" ? "Yes" : "No", "\n";
// Yes


// Equal
echo 10 == 10 ? "Yes" : "No", "\n";
// Yes


// Identical (Equal value and type)
echo 10 === "10" ? "Yes" : "No", "\n";
// No


// Identical (Equal value and type)
echo 10 === 10 ? "Yes" : "No", "\n";
// Yes
